#En python existen distintos tipos de numeros. Enteros, Flotantes, Complejos (Complex), Boleanos
number_one = 10
number_two = 4
result = number_one + number_two
print( "Suma ", result ) 
result = number_one - number_two
print( "Resta ", result ) 
result = number_one * number_two
print( "Multiplicacion ", result ) 
result = number_one / number_two
print( "Division ", result ) #Regresa un numero flotante
result = number_one // number_two
print( "Division Numero entero ", result ) #Regresa una division con numero entero
result = number_one ** number_two
print( "Exponencial ", result ) #Regresa un numero a su exponencial 